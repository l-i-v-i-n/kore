//! # kore
//!
//! Convert JSON values into **`.kore`** — a compact, human-readable text
//! format for representing API responses and structured data — without
//! losing any information from the original JSON.
//!
//! This is a Rust port of the [`kore-js`](https://github.com/kore-lang/kore-js)
//! reference implementation, kept output-compatible for the common cases
//! (flat objects, tables, lists, scalars, envelope-style API responses)
//! while fixing a couple of small correctness issues found in the original.
//! See the README's "Differences from kore-js" section for the full list.
//!
//! ## Quick start
//!
//! ```
//! use kore::{to_kore, KoreOptions};
//! use serde_json::json;
//!
//! let hikes = json!([
//!     { "id": 1, "name": "Blue Lake Trail", "km": 7.5, "sunny": true },
//!     { "id": 2, "name": "Ridge Overlook",  "km": 9.2, "sunny": false },
//! ]);
//!
//! let result = to_kore(&hikes, &KoreOptions::new("hikes"));
//! println!("{}", result.kore);
//! ```
//!
//! Output:
//!
//! ```text
//! hikes[id, name, km, sunny]:
//!   1 | Blue Lake Trail | 7.5 | true
//!   2 | Ridge Overlook  | 9.2 | false
//! ```
//!
//! ## Parsing a JSON string directly
//!
//! ```
//! use kore::{to_kore_from_str, KoreOptions};
//!
//! let result = to_kore_from_str(
//!     r#"[{"id":1,"name":"ana"}]"#,
//!     &KoreOptions::new("users"),
//! ).unwrap();
//! assert!(result.kore.contains("users["));
//! ```
//!
//! ## Type inference
//!
//! Setting [`KoreOptions::infer_types`] adds an `@types { ... }` block above
//! a table, inferring a narrow type per column from the data itself:
//!
//! ```
//! use kore::{to_kore, KoreOptions};
//! use serde_json::json;
//!
//! let data = json!([{ "id": 1, "km": 7.5, "active": true }]);
//! let result = to_kore(&data, &KoreOptions::new("items").infer_types(true));
//! assert!(result.kore.contains("@types"));
//! ```

